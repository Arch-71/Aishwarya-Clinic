                    <div class="card-body">
                        <h2 class="card-title text-center"><?php echo $title; ?></h2>
                        <?php if ($error): ?>
                            <div class="alert alert-danger"><?php echo $error; ?></div>
                        <?php endif; ?>
                        <form method="POST" action="">
